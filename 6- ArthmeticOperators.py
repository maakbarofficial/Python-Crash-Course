# Operators + - * /

print(5 + 2)

print(5 * 2)

print(5 / 2)

# remove decimal from answer
print(5 // 2)

# remainder/modular
print(5 % 2)

# power
print(5**2)

# equal to below
i = 5
i = i + 2

# this equals to above
i += 2

# we also can
i -= 2

print(i)


# Operator Precedence
# * precedence is higher than +
result = 2 + 3 * 5

print(result)

# bracket >
result = (2 + 3) * 5

print(result)
